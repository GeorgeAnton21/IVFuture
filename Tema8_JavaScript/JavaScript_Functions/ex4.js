//The Geometrizer
function calcCircumference(radius){
    console.log(`The circumference is ${(2*Math.PI*radius)}`)
}


function calcArea(radius){
    console.log(`The area is ${(Math.PI*Math.pow(radius,2))}`)
}



calcCircumference(10);
calcCircumference(2);
calcCircumference(6);

calcArea(6);
calcArea(19);
calcArea(56);
